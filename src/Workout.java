public class Workout {
    String exerciseName;
    int sets;
    int reps;
    double weightUsed;

    public Workout(String exerciseName, int sets, int reps, double weightUsed) {
        this.exerciseName = exerciseName;
        this.sets = sets;
        this.reps = reps;
        this.weightUsed = weightUsed;
    }
}
